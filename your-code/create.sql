USE lab_mysql;

CREATE TABLE CARS (Id INT PRIMARY KEY AUTO_INCREMENT, VIN VARCHAR(17), Manufacturer VARCHAR(25),
Model VARCHAR(25), `Year` date, color VARCHAR(15));

CREATE TABLE SALESPERSON (Id INT PRIMARY KEY AUTO_INCREMENT, Staff_ID numeric,
`Name` VARCHAR(15), Store VARCHAR(15));

ALTER TABLE SALESPERSON AUTO_INCREMENT = 00001;

CREATE TABLE CLIENTS (Id INT PRIMARY KEY AUTO_INCREMENT, Costumer_id numeric, 
`Name` VARCHAR(50) NOT NULL, Phone VARCHAR(15), Email VARCHAR(50), Address VARCHAR(50), City VARCHAR(50),
State VARCHAR(50), Country VARCHAR(50), CEP numeric);

ALTER TABLE CLIENTS AUTO_INCREMENT = 10001;

CREATE TABLE INVOICES (Id INT PRIMARY KEY AUTO_INCREMENT, Invoice_Number numeric, `Date` date,
Car_Id INT, FOREIGN KEY (Car_Id) REFERENCES CARS(Id), Costumer_Id INT, FOREIGN KEY (Costumer_Id) REFERENCES CLIENTS(Id),
Employee_Id INT, FOREIGN KEY (Employee_Id) REFERENCES SALESPERSON(Id));